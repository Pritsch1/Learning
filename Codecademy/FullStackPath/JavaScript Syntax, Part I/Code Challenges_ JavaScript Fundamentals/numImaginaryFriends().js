function numImaginaryFriends(friends) {
  return Math.ceil(friends * 0.25);
}
console.log(numImaginaryFriends(20));
console.log(numImaginaryFriends(10)); 
console.log(numImaginaryFriends(5));
console.log(numImaginaryFriends(3));
console.log(numImaginaryFriends(1));
console.log(numImaginaryFriends(0));
console.log(numImaginaryFriends(-1));
console.log(numImaginaryFriends(12.254));
